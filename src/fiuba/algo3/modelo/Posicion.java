package fiuba.algo3.modelo;

public class Posicion {
    
    private int columna;
    private int fila;

    public void setColumna(int columna) {
        this.columna = columna;
    }

    public void setFila(int fila) {
        this.fila = fila;
    }

    public int getColumna() {
        return this.columna;
    }

    public int getFila() {
        return this.fila;
    }
}
